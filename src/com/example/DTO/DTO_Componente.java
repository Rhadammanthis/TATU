package com.example.DTO;

public class DTO_Componente {
	
	private String id_componente;
	private String id_dieta;
	private String dia;
	private String alimento;
	private String descripcion;
	
	public String getId_componente() {
		return id_componente;
	}
	public void setId_componente(String id_componente) {
		this.id_componente = id_componente;
	}
	public String getId_dieta() {
		return id_dieta;
	}
	public void setId_dieta(String id_dieta) {
		this.id_dieta = id_dieta;
	}
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	public String getAlimento() {
		return alimento;
	}
	public void setAlimento(String alimento) {
		this.alimento = alimento;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

}
